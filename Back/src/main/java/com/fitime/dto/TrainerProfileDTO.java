package com.fitime.dto;

public class TrainerProfileDTO {

	private int trainer_idx;
	private String user_id;
	private int center_idx;
	private String career;
	public int getTrainer_idx() {
		return trainer_idx;
	}
	public void setTrainer_idx(int trainer_idx) {
		this.trainer_idx = trainer_idx;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getCenter_idx() {
		return center_idx;
	}
	public void setCenter_idx(int center_idx) {
		this.center_idx = center_idx;
	}
	public String getCareer() {
		return career;
	}
	public void setCareer(String career) {
		this.career = career;
	}
	
	
	
}
